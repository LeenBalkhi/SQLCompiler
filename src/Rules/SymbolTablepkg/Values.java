package Rules.SymbolTablepkg;

import java.util.ArrayList;

public class Values {

    public ArrayList<Integer> numbers;
    public ArrayList<String> strings;
    public ArrayList<Double> doubles;
    public ArrayList<Character> characters;
    public ArrayList<Float> floats;
    public ArrayList<Symbol> symbols;

    public ArrayList<Integer> getNumbers() {
        return numbers;
    }

    public void setNumbers(ArrayList<Integer> numbers) {
        this.numbers = numbers;
    }

    public ArrayList<String> getStrings() {
        return strings;
    }

    public void setStrings(ArrayList<String> strings) {
        this.strings = strings;
    }

    public ArrayList<Double> getDoubles() {
        return doubles;
    }

    public void setDoubles(ArrayList<Double> doubles) {
        this.doubles = doubles;
    }

    public ArrayList<Character> getCharacters() {
        return characters;
    }

    public void setCharacters(ArrayList<Character> characters) {
        this.characters = characters;
    }

    public ArrayList<Float> getFloats() {
        return floats;
    }

    public void setFloats(ArrayList<Float> floats) {
        this.floats = floats;
    }

}
